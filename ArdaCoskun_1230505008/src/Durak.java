public class Durak {
    String ad;
    double[] veriler;

    // Constructor
    public Durak(String ad, double[] veriler) {
        this.ad = ad;
        this.veriler = veriler;
    }

    // Getter metodu
    public String getAd() {
        return ad;
    }

    // Veriler getter metodu
    public double[] getVeriler() {
        return veriler;
    }
}